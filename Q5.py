# Q.5 How will you compare two lists?

""" To compare two lists in python,you can use the == operater, which checks if the two lists have the same elements in the same order,"""
lists1 = [1,2,3]
lists2 = [1,2,4]


if lists1 == lists2:
   print("The lists are equal.")
else:
   print("The lists are not equal.")